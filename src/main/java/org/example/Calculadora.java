package org.example;

public class Calculadora {

    public Integer sumar(Integer unNum, Integer otroNum) {
        return unNum + otroNum;
    }

    public Integer multiplicar(Integer unNum, Integer otroNum ) {
        return unNum * otroNum;
    }

    public Integer restar(Integer unNum, Integer otroNum ) {
        return unNum - otroNum;
    }

    public Integer dividir(Integer unNum, Integer otroNum ) {
        return unNum / otroNum;
    }
}
